    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {

        View rootView = inflater.inflate(R.layout.fragment_hike_map, container, false);
        points = new ArrayList();
        rectOptions = new PolylineOptions();
        prev = null;
        mFusedLocationClient = LocationServices.getFusedLocationProviderClient(getContext());

        mMapView = (MapView) rootView.findViewById(R.id.Mapau);
        mMapView.onCreate(savedInstanceState);

        mMapView.onResume();

        try {
            MapsInitializer.initialize(getActivity().getApplicationContext());
        } catch (Exception e) {
            e.printStackTrace();
        }

        mMapView.getMapAsync(new OnMapReadyCallback() {
            @Override
            public void onMapReady(GoogleMap mMap) {
                googleMap = mMap;
                getDeviceLocation();
            }
        });

        locationManager = (LocationManager) getActivity().getSystemService(Context.LOCATION_SERVICE);

        return rootView;
    }

    @Override
    public void onLocationChanged(Location location) {

        LatLng current = new LatLng(location.getLatitude(), location.getLongitude());
        CameraUpdate update = CameraUpdateFactory.newLatLngZoom(current, 16);
        googleMap.animateCamera(update);

        if (MainActivity.getWalking()) {
            if (prev != null) {
                googleMap.addPolyline((new PolylineOptions())
                        .add(prev, current).width(6).color(Color.BLUE)
                        .visible(true));
            }else{
                googleMap.addMarker(new MarkerOptions().position(current));
            }
            points.add(current);
            prev = current;
        }
    }

    @Override
    public void onProviderDisabled(String provider) {

        Intent intent = new Intent(Settings.ACTION_LOCATION_SOURCE_SETTINGS);
        startActivity(intent);
        Toast.makeText(getContext(), "Gps is turned off!! ",
                Toast.LENGTH_SHORT).show();
    }

    @Override
    public void onProviderEnabled(String provider) {

        Toast.makeText(getContext(), "Gps is turned on!! ",
                Toast.LENGTH_SHORT).show();
    }

